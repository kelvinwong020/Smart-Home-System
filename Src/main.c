void fan_speed_auto(unsigned int temperature){
	if(temperature<25){
		user_pwm_setvalue(50); 		//0% duty cycle
	}else if(temperature>=25 && temperature <27){
		user_pwm_setvalue(864);			//30% duty cycle
	}else if(temperature>=27 && temperature<30){
		user_pwm_setvalue(1728);		//60% duty cycle
	}else{
		user_pwm_setvalue(2860);		//100% duty cycle
	}
	
}


