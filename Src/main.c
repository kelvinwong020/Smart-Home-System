	LCD_DrawString(0,0,"Initializing...");
	
	HAL_TIM_Base_Start(&htim1);
	HAL_TIM_PWM_Start(&htim3,TIM_CHANNEL_1);
	DHT11_Init();
	HAL_Delay(1000);
	
	//Wifi SSID, password, static IP
	ESP_Init("Buffalo-G-751","checkbuffalo","192.168.1.4");
	
	LCD_Clear(0,0,240,320,WHITE);
	
	
